  puts $magenta + "tool by hackertool" + $magenta
  puts $orange + "usage: 
ruby xscan.rb -t 216.18.19.XXX -s 80 -e 9000" + $orange
  puts ""
  puts $orange + "Options:" + $orange
  puts $orange + "  -t, --target      target ip or website domain" + $orange
  puts $orange + "  -s, --port-start  starting port number" + $orange
  puts $orange + "  -e, --port-end    ending port number" + $orange
  puts $orange + "  -h, --help        Show help message" + $orange
  exit
end

# Function to show usage error
def show_usage_error
  puts $red + "error you used the usage wrong" + $red
  puts $orange + "Usage: 
ruby xscan.rb -t <target> -s <port_start> -e <port_end>" + $orange
  exit(1)
end

# Main function
if __FILE__ == $0
  # Check if help is requested
  if ARGV.include?('-h') || ARGV.include?('--help')
    show_help
  end

  # Check if no arguments provided
  if ARGV.empty?
    puts $magenta + "tool by hackertool" + $magenta
    puts $orange + "Usage: 
ruby xscan.rb -t <target> -s <port_start> -e <port_end>" + $orange
    exit
  end

  # Parse command line arguments
  target = nil
  start_port = nil
  end_port = nil

  i = 0
  while i < ARGV.length
    case ARGV[i]
    when '-t', '--target'
      target = ARGV[i + 1]
      i += 2
    when '-s', '--port-start'
      start_port = ARGV[i + 1].to_i
      i += 2
    when '-e', '--port-end'
      end_port = ARGV[i + 1].to_i
      i += 2
    else
      show_usage_error
    end
  end

  # Validate all required arguments are present
  if target.nil? || start_port.nil? || end_port.nil?
    show_usage_error
  end

  # Validate port range
  if start_port < 1 || end_port > 65535 || start_port > end_port
    puts $red + "error: invalid port range! Ports must be between 1 and 65535, and start port must be <= end port." + $red
    exit(1)
  end

  start_time = Time.now
  puts $magenta + "tool by hackertool" + $magenta
  puts $yellow + "Scanning #{target} from port #{start_port} to #{end_port}..." + $yellow
  scan_ports(target, start_port, end_port)
  puts $green + "Scan done in #{(Time.now - start_time).round(2)} seconds." + $green
end