# Function to check if a port is open with timeout handling
def scan_port(ip, port)
  begin
    Timeout::timeout(1) do
      sock = TCPSocket.new(ip, port)
      sock.setsockopt(Socket::SOL_SOCKET, Socket::SO_RCVBUF, 1)
      service = PORTS_NAMES[port] || 'Unknown Service'
      puts $green + "Port #{port} is open (#{service})" + $green
      sock.close
    end
  rescue Timeout::Error, Errno::ECONNREFUSED, Errno::ETIMEDOUT, Errno::EHOSTUNREACH
    # Port is closed or unreachable
  rescue => e
    puts $red + "Error scanning port #{port}: #{e.message}" + $red
  end
end

# Function to scan a range of ports
def scan_ports(ip, start_port, end_port)
  threads = []
  (start_port..end_port).each do |port|
    threads << Thread.new { scan_port(ip, port) }
  end
  threads.each(&:join)
end

